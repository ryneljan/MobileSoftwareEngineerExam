        Assert.assertFalse("+63917123456".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917-123-4567".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63-917-123-4567".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917 123 4567".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63 917 123 4567".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917abcdefg".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63917abcdefg".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917123456a".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63917123456a".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917123456!".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63917123456!".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917123456#".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63917123456#".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917123456$".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63917123456$".isValidPhilippineMobileNumber())
