        Assert.assertFalse("+63917123456%".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917123456^".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63917123456^".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917123456&".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63917123456&".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917123456*".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63917123456*".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917123456(".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63917123456(".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917123456)".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63917123456)".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917123456-".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63917123456-".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917123456_".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63917123456_".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917123456+".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63917123456+".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917123456=".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63917123456=".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917123456?".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63917123456?".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917123456/".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63917123456/".isValidPhilippineMobileNumber())
        Assert.assertFalse("0917123456\\".isValidPhilippineMobileNumber())
        Assert.assertFalse("+63917123456\\".isValidPhilippineMobileNumber())
