package com.raineru.msee

import org.junit.Assert
import org.junit.Test

class PhilippineMobileNumberValidatorTest {

    @Test
    fun testValidPhilippineMobileNumbers() {
        Assert.assertTrue("09171234567".isValidPhilippineMobileNumber())
        Assert.assertTrue("09289876543".isValidPhilippineMobileNumber())
        Assert.assertTrue("+639171234567".isValidPhilippineMobileNumber())
        Assert.assertTrue("+639289876543".isValidPhilippineMobileNumber())
        Assert.assertTrue("09991112222".isValidPhilippineMobileNumber())
        Assert.assertTrue("+639991112222".isValidPhilippineMobileNumber())
    }

    @Test
    fun testInvalidPhilippineMobileNumbers() {
        Assert.assertFalse("".isValidPhilippineMobileNumber())
        Assert.assertFalse("1234567890".isValidPhilippineMobileNumber())
        Assert.assertFalse("091712345678".isValidPhilippineMobileNumber())
        Assert.assertFalse("+6391712345678".isValidPhilippineMobileNumber())
